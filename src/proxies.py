def get_valid_proxy(timeout=timeout, retries=400):
    random.shuffle(PROXIES)
    retries_left = retries
    for proxy in PROXIES:
        if retries_left == 0:
            break
        try:
            print('Trying proxy', proxy, '... (', retries_left, 'retries left)')
            response = requests.get('https://www.google.com', proxies={'http': proxy}, timeout=timeout)
            if response.status_code == 200:
                return {'http': proxy}
        except Exception as e:
            print("Error:", e)
        retries_left -= 1
    raise ValueError('No valid proxies found.')
